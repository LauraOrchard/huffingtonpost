		<h3>Passionate Reader/Commenter</h3>
		<blockquote>
			<ul>
				<li>How? From a moble phone or tablet, a Passionate Reader/Commenter will click through from a Tweet sent
					from a follower.</li>
				<li>When? 24/7</li>
				<li>Where? Anywhere a data connection is available.</li>
				<li>Purpose? Entertainment and mental sparring.</li>
				<li>Scenarios? Peer pressure to read and respond to the article, and then engage in a debate. This is the
				discussion forum.</li>
				<li>Necessary Steps?</li>
					<ol>
						<li>Receive Twitter with Huffpost link;</li>
						<li>Arrive at article page on http://www.huffingtonpost.com/2015/04/15/cheryls-birthday-answer_n_7067790.html;</li>
						<li>Read article by scrolling down page;</li>
						<li>Click on embedded media link to watch clip ;</li>
						<li>Scroll down to Comments section;</li>
						<li>Scroll down to Comments section to read posted comments;</li>
						<li>Click "Like" and "Follow" on various comments;</li>
						<li>Facebook login screen opens for user id and password;</li>
						<li>Click on Post Comment button;</li>
						<li>Write comment;</li>
						<li>Click "Post" button;</li>
						<li>Repeat.</li>
					</ol>
			</ul>
		</blockquote>
		<h3>Blogger</h3>
		<blockquote>
			<ul>
				<li>How? From a desktop or tablet via the home page of HuffPost.</li>
				<li>When? 24/7</li>
				<li>Where? Anywhere a data connection is available.</li>
				<li>Purpose? Searching for writing opportunities and collecting the writing of others to include in a blog post.
				The "discussion forum" is not HuffPost, but the Blogger's blog page.</li>
				<li>Scenarios? While browsing articles on the Huffington Post home page, will see the "Cheryl's Birthday
					Math Challenge" headline and will click on the image to open
					http://www.huffingtonpost.com/2015/04/15/cheryls-birthday-answer_n_7067790.html, will scroll to read
				and will email the article to their own address in order to include it in their next blog post.</li>
				<li>Necessary Steps?</li>
				<ol>
					<li>Click on image from home page of Huffington Post;</li>
					<li>Scroll down "Cheryl's Birthday" article;</li>
					<li>Click on media link to watch embedded clip;</li>
					<li>Scroll down to Comments section to read posted comments;</li>
					<li>Click "Like" and "Follow" on various comments;</li>
					<li>Facebook login screen opens for user id and password;</li>
					<li>Click on the Email icon to forward the article to their own address.</li>
				</ol>
			</ul>
		</blockquote>
